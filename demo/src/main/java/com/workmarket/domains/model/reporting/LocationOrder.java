package com.workmarket.domains.model.reporting;

public class LocationOrder implements java.io.Serializable  {

	/*
	 * Instance variables and constants
	 */
	private int row;
	private int index;
	private int column;
	private static final long serialVersionUID = -5363669433835791448L;

	/**
	 * @return the row
	 */
	public int getRow() {
		return row;
	}
	/**
	 * @param row the row to set
	 */
	public void setRow(int row) {
		this.row = row;
	}
	/**
	 * @return the index
	 */
	public int getIndex() {
		return index;
	}
	/**
	 * @param index the index to set
	 */
	public void setIndex(int index) {
		this.index = index;
	}
	/**
	 * @return the column
	 */
	public int getColumn() {
		return column;
	}
	/**
	 * @param column the column to set
	 */
	public void setColumn(int column) {
		this.column = column;
	}

}
